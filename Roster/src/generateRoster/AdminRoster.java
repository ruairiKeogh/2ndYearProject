    private void dayListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dayListMouseClicked
        String tmp = (String) dayList.getSelectedValue();

        String sql = "select * from Staff where name=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, tmp);
            rs = pst.executeQuery();
            if (rs.next()) {
                String add = rs.getString("staff_id");
                selectedStaffTextField.setText(add);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_dayListMouseClicked

